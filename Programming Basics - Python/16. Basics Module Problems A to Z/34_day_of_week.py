input_number = int(input())

if input_number == 1:
    print('Monday')
elif input_number == 2:
    print('Tuesday')
elif input_number == 3:
    print('Wednesday')
elif input_number == 4:
    print('Thursday')
elif input_number == 5:
    print('Friday')
elif input_number == 6:
    print('Saturday')
elif input_number == 7:
    print('Sunday')
else:
    print('Error')